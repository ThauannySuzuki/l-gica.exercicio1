/* 11) Escreva um programa que calcule a média aritmética entre três números reais inseridos pelo usuário. */

let n1 = parseFloat(prompt("Digite um número: "))
let n2 = parseFloat(prompt("Digite um número: "))
let n3 = parseFloat(prompt("Digite um número: "))

let resultado = (n1+n2+n3) / 3 

document.write("A media aritmetrica dos três números é: " + resultado)