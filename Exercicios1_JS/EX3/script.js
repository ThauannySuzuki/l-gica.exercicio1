/* 3) Crie um programa que leia a base e a altura de um triângulo e calcule sua área. */

let base = parseFloat(prompt("Digite a base do triangulo: "))
let altura = parseFloat(prompt("Digite a altura do triângulo: "))
let resul = (base*altura)/2

document.write("O valor da área do triângulo é de: " + resul)
